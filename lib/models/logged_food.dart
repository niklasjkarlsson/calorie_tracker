class LoggedFood {
  final String name;
  final double amount;
  final double kcal;
  final double protein;
  final double carbs;
  final double fat;

  LoggedFood({
    required this.name,
    required this.amount,
    required this.kcal,
    required this.protein,
    required this.carbs,
    required this.fat,
  });
}
